    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        
        int count = 0;
        
        String uname = username.getText();
        String pass  = password.getText();
        String pos   = position.getSelectedItem().toString();
        
        
        System.out.println(uname);
        System.out.println(pass);
        System.out.println(pos);
        
        
        String sql = "SELECT id,username,password,position FROM home WHERE username='"+uname+"' AND password='"+pass+"' AND position='"+pos+"'";
        
        try {
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            while(rs.next()){
                post = rs.getString(4);
                
                if(post.equals("admin")){
                    JOptionPane.showMessageDialog(rootPane, "Success");
                    post.equals("");
                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Username and Password you entered are incorrect!");
                }
            }
            
            System.out.println(post);
            
            
            
        } catch (SQLException e) {
            System.out.println(e);
        }
        
       
    }//GEN-LAST:event_loginActionPerformed

