    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        JOptionPane.showConfirmDialog(null, "Do You Really Want to Update");
        con = DBconnect.connect();
        String sql = "UPDATE orders SET Discription =?, ItemID =?, Quantity =?, OrderItem =?, ItemDetails =?, Discount =?, UnitPrice =?, TotalPrice =? WHERE OrderID =?";
        try{
                pst = con.prepareStatement(sql);
                pst.setString(1, upDescription.getText());
                pst.setString(2, upItemid.getText());
                pst.setString(3, upquantity.getSelectedItem().toString());
                pst.setString(4, uporderItem.getSelectedItem().toString());
                pst.setString(5, upIdetails1.getText());
                pst.setString(6, updiscount.getText());
                pst.setString(7, upUprice.getText());
                pst.setString(8, upTprice.getText());
                pst.setString(9, OrderID.getText());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Order UPDATED Successfully");
                
             
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }       
        
        ShowDetails();
        
    }//GEN-LAST:event_jButton1ActionPerformed

