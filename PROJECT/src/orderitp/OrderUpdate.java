    private void jTable5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable5MouseClicked
        
          int index = jTable5.getSelectedRow();

        TableModel model = jTable5.getModel();

       
       
        String Discription = model.getValueAt(index, 1).toString();
        String ItemID = model.getValueAt(index, 2).toString();
        String Quantity = model.getValueAt(index, 3).toString();
        String OrderItem = model.getValueAt(index, 4).toString();
        String ItemDetails = model.getValueAt(index, 5).toString();
        String Discount = model.getValueAt(index, 6).toString();
        String UnitPrice = model.getValueAt(index, 7).toString();
        String TotalPrice = model.getValueAt(index, 8).toString();
        

       
        upDescription.setText(Discription);
        upItemid.setText(ItemID);
        upquantity.setSelectedItem(Quantity);
        uporderItem.setSelectedItem(OrderItem);
        upIdetails1.setText(ItemDetails);
        updiscount.setText(Discount);
        upUprice.setText(UnitPrice );
        upTprice.setText(TotalPrice);
       
    }//GEN-LAST:event_jTable5MouseClicked

