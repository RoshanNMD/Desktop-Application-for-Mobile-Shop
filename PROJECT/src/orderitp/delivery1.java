    public ArrayList <user> userlist(){
        ArrayList <user> userlist = new ArrayList <>();
        String names = searchbox.getText();
        
     try{
      String sql = "select * from delivary where cName like \"%"+names+"%\"";
      pst = con.prepareStatement(sql);
      rs = pst.executeQuery();
      user user;
      while(rs.next()){
          user = new user(rs.getInt("cid"),rs.getString("cName"),rs.getString("cAddress"),rs.getString("description"),rs.getString("iName"),rs.getString("vehicleId"),rs.getString("driverId"),rs.getString("orderId"),rs.getString("Quantity"));
          userlist.add(user);
      }
       
    }catch(Exception e){
         e.printStackTrace();
   }
     return userlist;
    }
    
    
   public void displayuser()
  {
      ArrayList<user> list = userlist();
      DefaultTableModel model = (DefaultTableModel) dtable.getModel();
      Object[] row = new Object[9];
      model.setRowCount(0);
      for (int i=0;i<list.size();i++){
          row[0]=list.get(i).getcid();
          row[1]=list.get(i).getname();
          row[2]=list.get(i).getaddress();
          row[3]=list.get(i).getdes();
          row[4]=list.get(i).getiname();
          row[5]=list.get(i).getvid();
          //row[6]=list.get(i).getavid();
          row[6]=list.get(i).getdid();
          row[7]=list.get(i).getdname();
          row[8]=list.get(i).getquantity();
          model.addRow(row);
      }
  } 
